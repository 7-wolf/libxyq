	const sMap2* ctt::getMap(string name)
	{
		map<string, int> musics;
		set<string> samenames;
		const auto& ms = ctt::getMaps();
		for (const auto& m : ms)
		{
			if (m.name == name)
			{
				return &m;
			}
			continue;
			if (!m.jianzhus.empty())
			{
				// 建筑没有了...
				int x = 0;
			}

			const auto& it =  musics.find(m.music);
			if (it == musics.end())
			{
				musics.insert(make_pair(m.music, 1));
			}
			else
			{
				++it->second;
			}
			if (m.name == m.music)
			{
				samenames.insert(m.name);
			}
		}
		return nullptr;

		set<string> mus;
		for (auto& mu : musics)
		{
			if (samenames.find(mu.first) == samenames.end())
			{
				mus.insert(mu.first);
			}
		}

		set<string> m2s;
		for (auto mu : mus)
		{
			bool isf = false;
			for (const auto& m : ms)
			{
				if (mus.find(m.name) != mus.end())
				{
					isf = true;
					break;
				}
				if (!isf)
				{
					m2s.insert(m.name);
				}

			}
		}


		return nullptr;
	}
